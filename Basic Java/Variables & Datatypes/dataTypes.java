public class dataTypes {
    public static void main(String[] args) {
        // DataTypes:
        // according to name, datatype is used to define the type of the data
        // thre are mainly two types of datatypes

        // 1: Primitive Dataype
        // those type of data which is pre-existing
        // we do not need to make them

        byte num1 = 23; // size occupies: 1 byte
        short num2 = 233; // size occupies: 2 bytes
        int num3 = 2333; // size occupies: 4 bytes
        long num4 = 23333; // size occupies: 8 bytes
        float num5 = 3.14f; // size occupies: 4 bytes
        double num6 = 3343.232; // size occupies: 8 bytes
        boolean condition = true; // size occupies: 1 byte
        char symbol = '@'; //// size occupies: 2 byte

        System.out.println(num1);
        System.out.println(num2);
        System.out.println(num3);
        System.out.println(num4);
        System.out.println(num5);
        System.out.println(num6);
        System.out.println(condition);
        System.out.println(symbol);

        // 2: Non-Premitive Datatype
        // those type of data which user need to create
        // for ex. String,Array,Class,Object,etc.
    }
}
