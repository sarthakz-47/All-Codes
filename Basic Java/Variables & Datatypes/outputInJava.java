public class outputInJava {
    public static void main(String[] args) {
        // output is given using the statement
        // System.out.println("")
        System.out.println("Ro-");

        // when we don't need to go on next line
        // System.out.print("")
        System.out.print("Ko");
        System.out.print("-Ro");

        // we can alternatively use \n to print into next line
        System.out.print("\n");
        System.out.print("1\n");
        System.out.print("2");
    }
}
