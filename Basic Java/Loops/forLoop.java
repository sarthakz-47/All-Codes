package Loops;

public class forLoop {
    public static void main(String[] args) {
        // Now what is for loop?
        // it is most widely used and efficeient loop
        // syntax: for(initalization;condition;updation)

        for (int i = 1; i <= 10; i++) {
            System.out.print(i + " ");
        }

        // here i is intialized to 1
        // i will run untill it is less than 10
        // after one cycle it will update i by +1
    }
}
