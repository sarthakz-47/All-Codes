package Loops;

public class doWhileLoop {
    public static void main(String[] args) {
        // now what is do-While Loop?
        // Simmilar to while loop but we first do the work then check condtion

        int n = 5;
        do {
            n--;
            System.out.print(n + " ");
        } while (n > 0);
    }
}
