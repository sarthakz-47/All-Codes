package Operators;

public class arithimaticOp {
    public static void main(String[] args) {
        // Artihimatic Operators:
        // Addition, Substraction, Division, Multiplication and Modulo
        int a = 10;
        int b = 5;

        System.out.println(a + b); // gives sum of two numbers
        System.out.println(a - b); // gives diffrence of two numbers
        System.out.println(a * b); // gives product of two numbers
        System.out.println(a / b); // gives quotient of two numbers
        System.out.println(a % b); // gives remainder of two numbers

        // As simple as that!
    }
}
