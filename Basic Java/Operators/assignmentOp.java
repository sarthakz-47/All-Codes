package Operators;

public class assignmentOp {
    public static void main(String[] args) {
        // Assignment Operator:
        // includes =, +=, -=, *= and /=
        // it is used to assign a particular value to the variable

        int a = 10;
        System.out.println(a);
        a += 10;
        System.out.println(a);
        a -= 5;
        System.out.println(a);
        a *= 10;
        System.out.println(a);
        a /= 10;
        System.out.println(a);

        // Generally a+=10 means a = a + 10;
        // Simple!

    }
}
