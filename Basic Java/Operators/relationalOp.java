package Operators;

public class relationalOp {
    public static void main(String[] args) {
        // Reletional Operators:
        // these operators are used to comapre diffrent conditions
        int a = 10;
        int b = 5;

        System.out.println(a == b); // used to compare two numbers
        System.out.println(a > b); // used to check which is greater
        System.out.println(a < b); // used to check which is smaller
        System.out.println(a >= b); // used to check which is greater and equal to
        System.out.println(a <= b); // used to check which is less and equal to
        System.out.println(a != b); // used to check which is not euqal to

    }
}
