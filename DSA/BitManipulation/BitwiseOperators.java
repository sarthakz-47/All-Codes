package DSA.BitManipulation;

public class BitwiseOperators {
    public static void main(String[] args) {
        // 1) Bitwise AND:
        System.out.println(5 & 6);

        // 2) Bitwise OR:
        System.out.println(5 | 6);

        // 3) Bitwise XOR:
        System.out.println(5 ^ 6);

        // 4) Bitwise Complement:
        System.out.println(~5);

        // 5) Binary Left Shift:
        System.out.println(5 << 2);

        // 6) Binary Right Shift:
        System.out.println(5 >> 2);
    }
}
